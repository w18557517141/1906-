$('.listOne .listOne-1 ul li a').on('mouseover',function(){
    $(this).css({
        'color' : '#ed1957',
        'textDecoration' : 'underline' 
    })
})
$('.listOne .listOne-1 ul li a').on('mouseout',function(){
    $(this).css({
        'color' : '#000',
        'textDecoration' : 'none' 
    })
    $('.listOne .listOne-1 ul .color a').css('color','#ed1957')
})


